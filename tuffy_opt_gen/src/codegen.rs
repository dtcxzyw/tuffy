/// Collect unique binding names referenced by a list of patterns.
fn collect_binding_names(patterns: &[Pattern]) -> Vec<&str> {
    let mut bindings = Vec::new();
    for pattern in patterns {
        pattern.collect_bindings(&mut bindings);
    }
    bindings
}

/// Render a tuple expression that snapshots the current binding slots.
fn snapshot_tuple_expr(binding_names: &[&str]) -> String {
    match binding_names {
        [] => "()".to_string(),
        [name] => format!("bind_{name}"),
        _ => format!(
            "({})",
            binding_names
                .iter()
                .map(|name| format!("bind_{name}"))
                .collect::<Vec<_>>()
                .join(", ")
        ),
    }
}

/// Render the tuple targets used to restore binding slots from a snapshot.
fn restore_tuple_targets(binding_names: &[&str]) -> String {
    binding_names
        .iter()
        .map(|name| format!("bind_{name}"))
        .collect::<Vec<_>>()
        .join(", ")
}

/// Render a Rust expression that reconstructs a decimal `BigInt`.
///
/// # Errors
///
/// Returns an error if `value` is not a valid decimal integer literal.
fn bigint_expr(value: &str) -> Result<String, GenerateError> {
    if let Ok(n) = value.parse::<u64>() {
        return Ok(format!("BigInt::from({n}u64)"));
    }

    if let Ok(n) = value.parse::<i64>() {
        return Ok(format!("BigInt::from({n}i64)"));
    }

    let _: BigInt = value
        .parse()
        .map_err(|_| GenerateError::InvalidIntConstant(value.to_string()))?;
    Ok(format!("parse_decimal_bigint({value:?})"))
}

/// Render the runtime `ValueRewrite` expression for a replacement.
///
/// # Errors
///
/// Returns an error if the replacement tree uses an unsupported value form.
fn value_rewrite_expr(replacement: &Replacement) -> Result<String, GenerateError> {
    match replacement {
        Replacement::Binding { name } => Ok(format!("ValueRewrite::Existing(bind_{name}?)")),
        Replacement::BoolConst { value } => Ok(format!("ValueRewrite::ConstBool({value})")),
        Replacement::BoolNot { value } => Ok(format!(
            "ValueRewrite::Expr(ReplacementExpr::BoolNot(Box::new({})))",
            replacement_expr_tree(value)?
        )),
    }
}

/// Render the runtime `ReplacementExpr` tree for a replacement.
///
/// # Errors
///
/// Returns an error if the replacement tree uses an unsupported value form.
fn replacement_expr_tree(replacement: &Replacement) -> Result<String, GenerateError> {
    match replacement {
        Replacement::Binding { name } => Ok(format!("ReplacementExpr::Value(bind_{name}?)")),
        Replacement::BoolConst { value } => Ok(format!("ReplacementExpr::ConstBool({value})")),
        Replacement::BoolNot { value } => Ok(format!(
            "ReplacementExpr::BoolNot(Box::new({}))",
            replacement_expr_tree(value)?
        )),
    }
}

/// Render an `IntAnnotation` construction expression.
fn int_annotation_expr(annotation: IntAnnotationSpec) -> String {
    let (bit_width, signedness) = match annotation {
        IntAnnotationSpec::Signed { bits } => (bits, "IntSignedness::Signed"),
        IntAnnotationSpec::Unsigned { bits } => (bits, "IntSignedness::Unsigned"),
        IntAnnotationSpec::DontCare { bits } => (bits, "IntSignedness::DontCare"),
    };
    format!("IntAnnotation {{ bit_width: {bit_width}, signedness: {signedness} }}")
}

/// Convert an arbitrary rule name into a Rust identifier fragment.
fn rule_ident(name: &str) -> String {
    let mut ident = String::with_capacity(name.len());
    for ch in name.chars() {
        if ch.is_ascii_alphanumeric() {
            ident.push(ch.to_ascii_lowercase());
        } else {
            ident.push('_');
        }
    }
    if ident.chars().next().is_some_and(|ch| ch.is_ascii_digit()) {
        ident.insert(0, '_');
    }
    ident
}
